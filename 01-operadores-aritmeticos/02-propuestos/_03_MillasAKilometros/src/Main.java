/*
03. Realice un programa que convierta millas a kilómetros.
Se sabe que una milla equivale a 1.609344 kilómetros.
 */

public class Main {
    public static void main(String[] args) {
        float millas = 2500;
        float kilometros = millas * 1.609344f;
        System.out.println(kilometros+" km.");

    }
}